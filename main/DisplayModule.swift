  // ---- I2C plumbing (control byte 0x00 = command stream, 0x40 = data stream) ----

  func cmd(_ b: UInt8) {
    fm_i2c_begin_transmission(Int32(addr))
    fm_i2c_write(0x00); fm_i2c_write(Int32(b))
    _ = fm_i2c_end_transmission(1)
  }

  func setCursor(_ line: Int, _ colPx: Int) {
    let c = colPx + colOffset
    cmd(0xB0 | UInt8(line & 0x07))              // page
    cmd(0x00 | UInt8(c & 0x0F))                 // column low nibble
    cmd(0x10 | UInt8((c >> 4) & 0x0F))          // column high nibble
  }

  /// Stream `count` blank columns (used by clear — covers the full 132-col RAM so
  /// SH1106 edge columns can't keep power-on noise; extra bytes wrap harmlessly).
  func writeBlank(_ count: Int) {
    var written = 0
    var chunkLeft = 0
    for _ in 0..<count {
      if chunkLeft == 0 {
        if written > 0 { _ = fm_i2c_end_transmission(1) }
        fm_i2c_begin_transmission(Int32(addr))
        fm_i2c_write(0x40)
        chunkLeft = 24
      }
      fm_i2c_write(0)
      written += 1; chunkLeft -= 1
    }
    if written > 0 { _ = fm_i2c_end_transmission(1) }
  }

  /// Stream `count` glyph cells from `lineBuf` — cell/glyph width and font follow `smallFont`.
  func writeCells(_ count: Int) {
    let cell = cellPx, glyphW = glyphCols
    var written = 0
    var chunkLeft = 0
    for i in 0..<count {
      let c = Int(lineBuf[i])
      let inRange = c >= 0x20 && c <= 0x7E
      for k in 0..<cell {
        if chunkLeft == 0 {                     // ≤ 24 data bytes per transmission
          if written > 0 { _ = fm_i2c_end_transmission(1) }
          fm_i2c_begin_transmission(Int32(addr))
          fm_i2c_write(0x40)
          chunkLeft = 24
        }
        var byte: Int32 = 0
        if k < glyphW && inRange {
          byte = smallFont ? Int32(Self.tiny[(c - 0x20) * 4 + k]) : Int32(Self.font[(c - 0x20) * 5 + k])
        }
        fm_i2c_write(byte)
        written += 1; chunkLeft -= 1
      }
    }
    if written > 0 { _ = fm_i2c_end_transmission(1) }
  }

  /// Render `lineBuf[0..<n]` at (line, col), space-padded to the end of the line.
  func flushLine(_ line: Int, _ col: Int, _ n: Int) {
    if addr < 0 { return }
    let c0 = col < 0 ? 0 : (col >= COLS ? COLS - 1 : col)
    let width = COLS - c0
    var count = n
    if count > width { count = width }
    for i in count..<width { lineBuf[i] = 0x20 }   // pad to end of line
    setCursor(line & 7, c0 * cellPx)
    writeCells(width)
  }

  /// Render `len` bytes (each read via `get`) starting at (startLine, col), WRAPPING onto the
  /// lines below when the text is longer than the line — a long string continues downward
  /// instead of being truncated. Stops at the bottom of the panel; each written line is
  /// space-padded to its end so no stale characters remain.
  func flushWrapped(_ startLine: Int, _ col: Int, _ len: Int, _ get: (Int) -> UInt8) {
    if addr < 0 { return }
    var idx = 0
    var line = startLine < 0 ? 0 : startLine
    var c0 = col < 0 ? 0 : (col >= COLS ? COLS - 1 : col)
    while line < LINES {
      let width = COLS - c0
      var count = 0
      while count < width && idx < len { lineBuf[count] = get(idx) & 0x7F; count += 1; idx += 1 }
      for i in count..<width { lineBuf[i] = 0x20 }   // pad this line to its end
      setCursor(line & 7, c0 * cellPx)
      writeCells(width)
      if idx >= len { break }                         // everything shown
      line += 1; c0 = 0                               // continue on the next line from column 0
    }
  }

  func clear() {
    if addr < 0 { return }
    let saved = colOffset
    colOffset = 0                              // clear the whole RAM from absolute column 0
    for line in 0..<LINES { setCursor(line, 0); writeBlank(132) }
    colOffset = saved
    setCursor(0, 0)
  }

  // ---- number formatting (no Foundation in Embedded Swift) ----

  /// Write `value` as decimal into lineBuf[at...]; returns the new length.
  func putInt(_ value: Int32, at start: Int) -> Int {
    var i = start
    var v = value
    if v < 0 { if i < COLS { lineBuf[i] = 0x2D; i += 1 } }          // '-'
    var digits = [UInt8](repeating: 0, count: 11)
    var n = 0
    var mag = v < 0 ? (v == Int32.min ? UInt32(2147483648) : UInt32(-v)) : UInt32(v)
    repeat { digits[n] = UInt8(mag % 10) + 0x30; mag /= 10; n += 1 } while mag > 0
    while n > 0 && i < COLS { n -= 1; lineBuf[i] = digits[n]; i += 1 }
    return i
  }

  func handle(_ payload: [UInt8], _ length: Int) {
    if length < 1 { return }
    switch payload[0] {
    case 0x00:                                  // configure: i2c address [+ column offset]
      if length >= 2 {
        addr = Int32(payload[1] & 0x7F)
        colOffset = length >= 3 ? Int(payload[2] & 0x7F) : 0   // 2 = SH1106, 0 = SSD1306
        smallFont = length >= 4 && (payload[3] & 0x7F) != 0    // 1 = tiny 4×6 font
        fm_i2c_begin(Int32(I2C_SDA_PIN), Int32(I2C_SCL_PIN))
        // SSD1306 128×64 init (off → clocks/geometry → charge pump → on).
        let seq: [UInt8] = [0xAE, 0xD5, 0x80, 0xA8, 0x3F, 0xD3, 0x00, 0x40,
                            0x8D, 0x14, 0x20, 0x02, 0xA1, 0xC8, 0xDA, 0x12,
                            0x81, 0xCF, 0xD9, 0xF1, 0xDB, 0x40, 0xA4, 0xA6, 0xAF]
        for b in seq { cmd(b) }
        clear()
      }
    case 0x01:                                  // clear
      clear()
    case 0x02:                                  // print literal: line col len:2 text… (wraps)
      if length >= 5 {
        let line = Int(payload[1] & 0x07), col = Int(payload[2] & 0x7F)
        let n = Int(payload[3] & 0x7F) | (Int(payload[4] & 0x7F) << 7)
        let avail = max(0, length - 5)
        let count = n < avail ? n : avail
        flushWrapped(line, col, count) { payload[5 + $0] }
      }
    case 0x03:                                  // print string slot: line col slot (wraps)
      if length >= 4 {
        let line = Int(payload[1] & 0x07), col = Int(payload[2] & 0x7F)
        let slot = Int32(payload[3] & 0x7F)
        let len = Int(fm_snapshot_len(slot))
        if len > 0, let p = fm_snapshot_ptr(slot) {
          flushWrapped(line, col, len) { p[$0] }
        } else {
          flushLine(line, col, 0)               // empty slot: clear the line
        }
      }
    case 0x04:                                  // print int register: line col reg
      if length >= 4 {
        let line = Int(payload[1] & 0x07), col = Int(payload[2] & 0x7F)
        let n = putInt(scheduler.regs[Int(payload[3] & REG_MASK)], at: 0)
        flushLine(line, col, n)
      }
    case 0x05:                                  // print float register (2 decimals): line col freg
      if length >= 4 {
        let line = Int(payload[1] & 0x07), col = Int(payload[2] & 0x7F)
        var f = scheduler.fregs[Int(payload[3] & FREG_MASK)]
        // value ×100 rounded, rendered as int.int2; clamped so the conversion can't trap
        if f > 2.0e7 { f = 2.0e7 }; if f < -2.0e7 { f = -2.0e7 }
        let scaled = Int32(f * 100 + (f >= 0 ? 0.5 : -0.5))
        var i = putInt(scaled / 100, at: 0)
        if i < COLS { lineBuf[i] = 0x2E; i += 1 }                    // '.'
        var frac = scaled % 100; if frac < 0 { frac = -frac }
        if i < COLS { lineBuf[i] = UInt8(frac / 10) + 0x30; i += 1 }
        if i < COLS { lineBuf[i] = UInt8(frac % 10) + 0x30; i += 1 }
        flushLine(line, col, i)
      }
    default: break
    }
  }

  func tick() {}                                // display is command-driven; nothing periodic
}
