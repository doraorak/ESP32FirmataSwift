// Display module (id 0x04): SSD1306 128×64 OLED over I2C — tasks made visible.

/* ==== Display module — a ModuleHandler ====================================
   Renders text with a 5×7 font, 8 lines × 21 columns. The op set is aimed at
   tasks: print a device STRING SLOT (json.getString / createString results) or a
   REGISTER — so an offline task can fetch → extract → display. Every print pads
   with spaces to the end of the line (no ghost characters from longer old text).
   Ops:
     0x00 <addr>                        configure (I2C init + clear + on); addr usually 0x3C
     0x01                               clear the whole display
     0x02 <line> <col> <len:2> <text…>  print literal 7-bit ASCII
     0x03 <line> <col> <slot>           print snapshot/string slot content (0–11)
     0x04 <line> <col> <reg>            print R[reg] as decimal
     0x05 <line> <col> <freg>           print F[freg] with 2 decimals            */
